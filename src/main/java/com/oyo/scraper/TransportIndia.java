package com.oyo.scraper;

public class TransportIndia {
	private String name;
	private String address;
	private String phone;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public TransportIndia setName(String name) {
		this.name = name;
		return this;
	}

	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * @param address the address to set
	 */
	public TransportIndia setAddress(String address) {
		this.address = address;
		return this;
	}

	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}

	/**
	 * @param phone the phone to set
	 */
	public TransportIndia setPhone(String phone) {
		this.phone = phone;
		return this;
	}

}
